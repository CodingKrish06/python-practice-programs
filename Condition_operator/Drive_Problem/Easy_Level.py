# 1)Positive ,Negative or Zero - Bank Balance Status

N = int(input())
if N >= 0 :
    print("Positive")
elif N <= 0 :
    print("Negative")
else:
    print("Zero Balance")

# 2) Even or Odd - Token Number Checker

N = int(input())
if N % 2 == 0:
    print("Even")
else:
    print("Odd Token")

# 3)Pass or Fail - Student Result Checker

M = int(input())
if M >= 35:
    print("Pass")
else:
    print("Fail")

